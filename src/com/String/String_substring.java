package com.String;

public class String_substring {
    public static void main(String[] args) {

        String j = "JAVA IS AWESOME";

        System.out.println(j.substring(2));  // it will create substring from 2nd index of string.

        System.out.println("--------------------------");

        System.out.println(j.substring(2,4));      // it will create substring with 2nd & 3rd index.
    }
}
